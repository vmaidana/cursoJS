const a = 7
let b = 3
console.log(b)

b += a // atribuição aditiva. b = b + a
console.log(b)

b -=4 // atribuição substrativa. b = b - 4
console.log(b)

b *= 2 // atribuição multiplicativa. b = b * 2
console.log(b)

b /=2 // atribuição divisiva. b = b / 2
console.log(b)

b %=2 // atribuição modular. b = b mod 2
console.log(b)